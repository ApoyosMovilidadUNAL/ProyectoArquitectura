package co.unal.sm.dto;

public class Consumos {

	private Boolean funciona;
	private Float consumoi;
	private Float consumoe;

	public Boolean getFunciona() {
		return funciona;
	}

	public void setFunciona(Boolean funciona) {
		this.funciona = funciona;
	}

	public Float getConsumoi() {
		return consumoi;
	}

	public void setConsumoi(Float consumoi) {
		this.consumoi = consumoi;
	}

	public Float getConsumoe() {
		return consumoe;
	}

	public void setConsumoe(Float consumoe) {
		this.consumoe = consumoe;
	}

}
